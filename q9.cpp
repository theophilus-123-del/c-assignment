#include <iostream>
using namespace std;

int main()
{
    // Variable declaration
    int x;

    // Assignment operator
    x = 10;
    cout << "Assignment Operator (=): x = " << x << endl;

    // Equality operator
    if (x == 10)
    {
        cout << "Equality Operator (==): x is equal to 10" << endl;
    }

    return 0;
}
